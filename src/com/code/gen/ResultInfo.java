package com.code.gen;
public class ResultInfo {
	private String resultPackageName;
	private String className;
	private String resultDataPropName;
	private String resultMsgPropName;
	private String resultCodePropName;
	public String getResultPackageName() {
		return resultPackageName;
	}
	public void setResultPackageName(String resultPackageName) {
		this.resultPackageName = resultPackageName;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getResultDataPropName() {
		return resultDataPropName;
	}
	public void setResultDataPropName(String resultDataPropName) {
		this.resultDataPropName = resultDataPropName;
	}
	public String getResultMsgPropName() {
		return resultMsgPropName;
	}
	public void setResultMsgPropName(String resultMsgPropName) {
		this.resultMsgPropName = resultMsgPropName;
	}
	public String getResultCodePropName() {
		return resultCodePropName;
	}
	public void setResultCodePropName(String resultCodePropName) {
		this.resultCodePropName = resultCodePropName;
	}
}